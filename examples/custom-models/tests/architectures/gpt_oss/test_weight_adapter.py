def test_bias_mapping_precedes_weight_mapping():
    assert hasattr(weight_adapters, "GPT_OSS_SAFETENSOR_MAP")
    mapping_keys = list(weight_adapters.GPT_OSS_SAFETENSOR_MAP.keys())

    bias_key = "experts.gate_up_proj_bias"
    weight_key = "experts.gate_up_proj"
    assert bias_key in mapping_keys
    assert weight_key in mapping_keys
    assert mapping_keys.index(bias_key) < mapping_keys.index(weight_key)


